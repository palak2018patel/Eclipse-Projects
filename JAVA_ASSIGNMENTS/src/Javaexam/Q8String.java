package Javaexam;

public class Q8String {    
											//Using .toLowerCase method of the String class, I can write a given string in lowercase.
	public static void main(String[] args) {
		String s="I AM JAVA PROGRAMMER.";
		System.out.println("Given uppercase string is: "+s);
		System.out.println("Writing given string to lowercase: "+s.toLowerCase());
		
	}

}
